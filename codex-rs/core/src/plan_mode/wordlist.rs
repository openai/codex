#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_word_counts() {
        // Verify word list sizes match Claude Code
        assert!(ADJECTIVES.len() >= 200, "Expected ~220 adjectives");
        assert!(ACTIONS.len() >= 100, "Expected ~110 actions");
        assert!(NOUNS.len() >= 300, "Expected ~350 nouns");
    }

    #[test]
    fn test_total_combinations() {
        let total = ADJECTIVES.len() * ACTIONS.len() * NOUNS.len();
        // Should be ~8.5 million combinations
        assert!(
            total > 5_000_000,
            "Expected >5M combinations, got {total}"
        );
        println!(
            "Total combinations: {} ({} × {} × {})",
            total,
            ADJECTIVES.len(),
            ACTIONS.len(),
            NOUNS.len()
        );
    }

    #[test]
    fn test_no_duplicates() {
        use std::collections::HashSet;

        let adj_set: HashSet<_> = ADJECTIVES.iter().collect();
        assert_eq!(
            adj_set.len(),
            ADJECTIVES.len(),
            "Duplicate adjectives found"
        );

        let act_set: HashSet<_> = ACTIONS.iter().collect();
        assert_eq!(act_set.len(), ACTIONS.len(), "Duplicate actions found");

        let noun_set: HashSet<_> = NOUNS.iter().collect();
        assert_eq!(noun_set.len(), NOUNS.len(), "Duplicate nouns found");
    }
}
